/* 
  *****************************************************************************************************************
  * @file           : main.c
  * @brief          : Main program body
  * 
  * In STM32F429I-DISC1 Discovery board, I have externally connected UART1 Tx and UART3 Rx.
  * When the  push button connected in PA0(External interrupt configured) is pressed, UART1 will transmit a frame.
  * Uart3 will receive the frame in ISR and update the variables.
  * These variables will be checked in an infinite loop inside main function to toggle LED connected in PG13.
  * 
  * Open project f4series_UART2 in stm32cubeIde, build and flash to test the code. 
  ******************************************************************************************************************
*/

//Includes begin
#include "main.h"
#include <string.h>
//Includes end

//Macros begin
#define HEADER 0xAA
#define FOOTER 0xCC
//Macros end

//prototypes begin
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_USART1_UART_Init(void);
static void MX_USART3_UART_Init(void);
void generate_uart_frame(void);
//prototypes end

//Global variables begin
UART_HandleTypeDef huart1;
UART_HandleTypeDef huart3;

union tx_frame{

    struct TxFrame{
    	uint8_t header;
    	uint8_t msgid;
    	uint8_t datalen;
    	uint8_t data[2];
    	uint8_t checksum;
    	uint8_t footer;
    }frame;

    uint8_t tx_data[7];
}tx;

uint8_t rx_data[7];
//uint8_t tx_data[7] = {0xAA,0x21,0x02,0x01,0x03,0x21,0xCC};
uint8_t msgid, checksum,dlc,data1,data2;
//Global variable end

/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART1_UART_Init();
  MX_USART3_UART_Init();

  /* Generate the frame to be transmitted bu initializing the structure*/
  generate_uart_frame();

  /*Enable Reception of Uart3 in interrupt mode*/
  HAL_UART_Receive_IT(&huart3, (uint8_t*)&rx_data,sizeof(tx.tx_data));

  /* Infinite loop */
  while (1)
  {
    /*data bytes received in UART3 ISR is checked*/
	  if((data1 == 0x01) && (data2 ==0x03))
	  {
      /*Toggle the LED*/
		  HAL_GPIO_TogglePin(GPIOG,GPIO_PIN_13);
		  data1=0;
		  data2=0;
	  }
  }
}

/**
  * @brief Initialize TX frame
  * @retval None
  */
void generate_uart_frame(void)
{
    tx.frame.header = HEADER;
    tx.frame.msgid  = 0x21;
    tx.frame.datalen = 0x02;
    tx.frame.data[0] = 0x01;
    tx.frame.data[1] = 0x03;
    tx.frame.checksum = 0;
    for(int i=1;i<=2+tx.frame.datalen;i++)
    {
    	tx.frame.checksum ^= tx.tx_data[i];
    }

    tx.frame.footer = FOOTER;
}

/**
  * @brief ISR for External interrupt(PA0)
  * @retval None
  */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){
  if(GPIO_Pin==GPIO_PIN_0)
  {
    /*Transmit frame*/
	  HAL_UART_Transmit_IT(&huart1,tx.tx_data, sizeof(tx.tx_data));
  }
}

/**
  * @brief ISR for UART rception (UART3)
  * @retval None
  */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if((huart == &huart3))
  {
	  if((rx_data[0]== HEADER) && (rx_data[6] == FOOTER))
	  {
		  checksum = 0;
		  for(int i=1; i<=4;i++)
			  checksum ^= rx_data[i];

		  if(checksum == rx_data[5])
		  {
			  msgid = rx_data[1];
			  dlc = rx_data[2];
			  data1 = rx_data[3];      //Use for loop(if many)
			  data2 = rx_data[4];
		  }
	  }
	  HAL_UART_Receive_IT(&huart3,(uint8_t*)&rx_data,sizeof(tx.tx_data));
	  memset(rx_data,0, sizeof(rx_data));
  }
}

